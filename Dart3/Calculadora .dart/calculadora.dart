class Calculadora {
  num sum(num valor1, num valor2) {
    return valor1 + valor2;
  }
  num decrease(num valor1, num valor2) {
    return valor1 - valor2;
  }
  num division(num valor1, num valor2) {
    return valor1 / valor2;
  }
  num multiplication(num valor1, num valor2) {
    return valor1 * valor2;
  }


}
